#!/usr/bin/env python3
"""
Benchmark codeguard against AI-generated repositories from GitHub.

Usage:
    # First run — 50 repos, catch errors:
    python scripts/benchmark_ai_repos.py --count 50

    # Full run — 1000 repos:
    python scripts/benchmark_ai_repos.py --count 1000

    # Custom query:
    python scripts/benchmark_ai_repos.py --count 100 --query "chatgpt generated"

    # Resume from previous run (skips already-processed repos):
    python scripts/benchmark_ai_repos.py --count 1000 --resume

Output: scripts/benchmark_results.jsonl (one JSON object per repo)
Summary: scripts/benchmark_summary.json
"""

import argparse
import json
import os
import shutil
import subprocess
import sys
import tempfile
import time
from datetime import datetime
from pathlib import Path

# GitHub search queries that find AI-generated code
SEARCH_QUERIES = [
    "generated by chatgpt language:python",
    "generated by claude language:python",
    "copilot generated language:python",
    "vibe coded language:python",
    "ai generated language:python",
    "built with cursor language:python",
    "gpt-4 generated language:python",
    "chatgpt python project",
    "ai coded python",
    "llm generated python",
]

SCRIPT_DIR = Path(__file__).parent
RESULTS_FILE = SCRIPT_DIR / "benchmark_results.jsonl"
SUMMARY_FILE = SCRIPT_DIR / "benchmark_summary.json"
ERRORS_FILE = SCRIPT_DIR / "benchmark_errors.log"

# Find codeguard binary
CODEGUARD_BIN = None
for candidate in [
    SCRIPT_DIR.parent / "target" / "release" / "codeguard",
    SCRIPT_DIR.parent / "target" / "debug" / "codeguard",
]:
    if candidate.exists():
        CODEGUARD_BIN = str(candidate)
        break


def search_github_repos(query: str, count: int, page: int = 1) -> list[dict]:
    """Search GitHub for repos matching query. Returns list of {name, clone_url, stars, description}."""
    per_page = min(count, 30)
    cmd = [
        "gh", "api", "search/repositories",
        "-X", "GET",
        "-f", f"q={query}",
        "-f", f"per_page={per_page}",
        "-f", f"page={page}",
        "-f", "sort=updated",
    ]
    try:
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
        if result.returncode != 0:
            print(f"  gh api error: {result.stderr[:200]}", file=sys.stderr)
            return []
        data = json.loads(result.stdout)
        items = data.get("items", [])
        return [
            {
                "full_name": r["full_name"],
                "clone_url": r["clone_url"],
                "stars": r.get("stargazers_count", 0),
                "description": (r.get("description") or "")[:200],
                "language": r.get("language", ""),
                "size_kb": r.get("size", 0),
                "updated_at": r.get("updated_at", ""),
            }
            for r in items
            if r.get("language") == "Python" and r.get("size", 0) < 100_000  # skip huge repos
        ]
    except (subprocess.TimeoutExpired, json.JSONDecodeError) as e:
        print(f"  search error: {e}", file=sys.stderr)
        return []


def collect_repos(count: int, existing_names: set[str]) -> list[dict]:
    """Collect unique repos from multiple search queries."""
    repos = []
    seen = set(existing_names)

    for query in SEARCH_QUERIES:
        if len(repos) >= count:
            break
        print(f"  Searching: '{query}'...")
        for page in range(1, 6):  # up to 5 pages per query
            if len(repos) >= count:
                break
            results = search_github_repos(query, 100, page)
            if not results:
                break
            for r in results:
                if r["full_name"] not in seen:
                    seen.add(r["full_name"])
                    repos.append(r)
                    if len(repos) >= count:
                        break
            time.sleep(1)  # rate limit

    print(f"  Collected {len(repos)} unique repos")
    return repos[:count]


def clone_repo(clone_url: str, dest: str, timeout: int = 60) -> bool:
    """Shallow clone a repo. Returns True on success."""
    try:
        subprocess.run(
            ["git", "clone", "--depth", "1", "--single-branch", clone_url, dest],
            capture_output=True, timeout=timeout,
        )
        return os.path.isdir(dest)
    except subprocess.TimeoutExpired:
        return False


def run_codeguard(repo_dir: str, timeout: int = 120) -> dict:
    """Run codeguard on a repo directory. Returns parsed results."""
    cmd = [
        CODEGUARD_BIN, "check", repo_dir,
        "--offline",
        "--format", "json",
    ]
    try:
        start = time.time()
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=timeout)
        elapsed = time.time() - start

        # Count Python files
        py_files = sum(1 for _ in Path(repo_dir).rglob("*.py"))

        # Parse JSON output (may be empty if no issues)
        diagnostics = []
        if result.stdout.strip():
            try:
                diagnostics = json.loads(result.stdout)
            except json.JSONDecodeError:
                pass

        # Parse the note line from stderr for offline summary
        stderr_text = result.stderr or ""
        note_line = ""
        for line in stderr_text.split("\n"):
            if "not verified" in line:
                note_line = line.strip()

        # Count by rule
        rule_counts = {}
        for d in diagnostics:
            code = d.get("code", "???")
            rule_counts[code] = rule_counts.get(code, 0) + 1

        return {
            "success": True,
            "py_files": py_files,
            "total_issues": len(diagnostics),
            "rule_counts": rule_counts,
            "elapsed_sec": round(elapsed, 2),
            "exit_code": result.returncode,
            "note": note_line,
            "error": None,
        }
    except subprocess.TimeoutExpired:
        return {
            "success": False,
            "py_files": 0,
            "total_issues": 0,
            "rule_counts": {},
            "elapsed_sec": timeout,
            "exit_code": -1,
            "note": "",
            "error": "timeout",
        }
    except Exception as e:
        return {
            "success": False,
            "py_files": 0,
            "total_issues": 0,
            "rule_counts": {},
            "elapsed_sec": 0,
            "exit_code": -1,
            "note": "",
            "error": str(e),
        }


def process_repo(repo: dict, work_dir: str) -> dict:
    """Clone, scan, and clean up a single repo."""
    repo_dir = os.path.join(work_dir, repo["full_name"].replace("/", "_"))

    # Clone
    if not clone_repo(repo["clone_url"], repo_dir):
        return {
            **repo,
            "scan": {
                "success": False, "error": "clone_failed",
                "py_files": 0, "total_issues": 0, "rule_counts": {},
                "elapsed_sec": 0, "exit_code": -1, "note": "",
            },
        }

    # Scan
    scan = run_codeguard(repo_dir)

    # Clean up
    shutil.rmtree(repo_dir, ignore_errors=True)

    return {**repo, "scan": scan}


def compute_summary(results: list[dict]) -> dict:
    """Compute aggregate statistics from results."""
    successful = [r for r in results if r["scan"]["success"]]
    total_repos = len(results)
    total_success = len(successful)
    total_files = sum(r["scan"]["py_files"] for r in successful)
    total_issues = sum(r["scan"]["total_issues"] for r in successful)
    total_time = sum(r["scan"]["elapsed_sec"] for r in successful)

    # Aggregate rule counts
    all_rules = {}
    for r in successful:
        for code, count in r["scan"]["rule_counts"].items():
            all_rules[code] = all_rules.get(code, 0) + count

    # Top issues
    top_rules = sorted(all_rules.items(), key=lambda x: -x[1])

    # Repos with most issues
    top_repos = sorted(successful, key=lambda r: -r["scan"]["total_issues"])[:20]

    # Error analysis
    errors = [r for r in results if not r["scan"]["success"]]
    error_types = {}
    for r in errors:
        err = r["scan"].get("error", "unknown")
        error_types[err] = error_types.get(err, 0) + 1

    return {
        "timestamp": datetime.now().isoformat(),
        "total_repos": total_repos,
        "successful_scans": total_success,
        "failed_scans": len(errors),
        "error_breakdown": error_types,
        "total_python_files": total_files,
        "total_issues_found": total_issues,
        "avg_issues_per_repo": round(total_issues / max(total_success, 1), 1),
        "avg_issues_per_file": round(total_issues / max(total_files, 1), 2),
        "total_scan_time_sec": round(total_time, 1),
        "avg_time_per_repo_sec": round(total_time / max(total_success, 1), 2),
        "rule_totals": dict(top_rules),
        "top_repos_by_issues": [
            {
                "name": r["full_name"],
                "stars": r["stars"],
                "py_files": r["scan"]["py_files"],
                "issues": r["scan"]["total_issues"],
                "top_rules": dict(sorted(r["scan"]["rule_counts"].items(), key=lambda x: -x[1])[:5]),
            }
            for r in top_repos
        ],
    }


def main():
    parser = argparse.ArgumentParser(description="Benchmark codeguard on AI-generated GitHub repos")
    parser.add_argument("--count", type=int, default=50, help="Number of repos to scan")
    parser.add_argument("--resume", action="store_true", help="Skip already-processed repos")
    parser.add_argument("--query", type=str, help="Override search query (single)")
    args = parser.parse_args()

    if not CODEGUARD_BIN:
        print("ERROR: codeguard binary not found. Run: cargo build --release", file=sys.stderr)
        sys.exit(1)

    print(f"Using codeguard: {CODEGUARD_BIN}")

    # Check gh CLI
    try:
        subprocess.run(["gh", "auth", "status"], capture_output=True, timeout=10)
    except (FileNotFoundError, subprocess.TimeoutExpired):
        print("ERROR: gh CLI not found or not authenticated. Run: gh auth login", file=sys.stderr)
        sys.exit(1)

    # Load existing results if resuming
    existing = {}
    if args.resume and RESULTS_FILE.exists():
        with open(RESULTS_FILE) as f:
            for line in f:
                try:
                    r = json.loads(line)
                    existing[r["full_name"]] = r
                except (json.JSONDecodeError, KeyError):
                    pass
        print(f"Loaded {len(existing)} existing results")

    # Override query if specified
    global SEARCH_QUERIES
    if args.query:
        SEARCH_QUERIES = [args.query]

    # Collect repos
    print(f"\n=== Collecting {args.count} repos ===")
    repos = collect_repos(args.count, set(existing.keys()))

    if not repos:
        print("No repos found to scan")
        if existing:
            print(f"Have {len(existing)} existing results, generating summary...")
            summary = compute_summary(list(existing.values()))
            with open(SUMMARY_FILE, "w") as f:
                json.dump(summary, f, indent=2)
            print(f"Summary written to {SUMMARY_FILE}")
        return

    # Process repos
    print(f"\n=== Scanning {len(repos)} repos ===")
    results = list(existing.values())  # start with existing

    with tempfile.TemporaryDirectory(prefix="codeguard_bench_") as work_dir:
        for i, repo in enumerate(repos):
            progress = f"[{i+1}/{len(repos)}]"
            print(f"{progress} {repo['full_name']} ({repo['stars']}★, ~{repo['size_kb']}KB)...", end=" ", flush=True)

            result = process_repo(repo, work_dir)
            results.append(result)

            scan = result["scan"]
            if scan["success"]:
                print(f"{scan['py_files']} files, {scan['total_issues']} issues, {scan['elapsed_sec']}s")
            else:
                print(f"FAILED: {scan.get('error', 'unknown')}")
                with open(ERRORS_FILE, "a") as ef:
                    ef.write(f"{datetime.now().isoformat()} {repo['full_name']}: {scan.get('error')}\n")

            # Append to JSONL (incremental save)
            with open(RESULTS_FILE, "a") as f:
                f.write(json.dumps(result) + "\n")

            # Rate limit
            time.sleep(0.5)

    # Generate summary
    print(f"\n=== Summary ({len(results)} repos) ===")
    summary = compute_summary(results)

    with open(SUMMARY_FILE, "w") as f:
        json.dump(summary, f, indent=2)

    # Print summary
    print(f"Repos scanned: {summary['successful_scans']}/{summary['total_repos']}")
    print(f"Python files:  {summary['total_python_files']}")
    print(f"Total issues:  {summary['total_issues_found']}")
    print(f"Avg per repo:  {summary['avg_issues_per_repo']}")
    print(f"Avg per file:  {summary['avg_issues_per_file']}")
    print(f"Scan time:     {summary['total_scan_time_sec']}s")
    print(f"\nTop rules:")
    for code, count in list(summary["rule_totals"].items())[:15]:
        print(f"  {code}: {count}")
    if summary["error_breakdown"]:
        print(f"\nErrors:")
        for err, count in summary["error_breakdown"].items():
            print(f"  {err}: {count}")

    print(f"\nResults: {RESULTS_FILE}")
    print(f"Summary: {SUMMARY_FILE}")


if __name__ == "__main__":
    main()
